
# 1st Method --
Name = "Jhon"
Age = 26
Salary = 230004
print("Name is: %s : Age is %d :Salary is %g" %(Name,Age,Salary))

# 2nd Method using curly braces{}--
Name = "Alberto Del Rio"
Age = 26
Salary = 23000
print("Name is {}  Age {}  Salary {:,d}" .format(Name,Age,Salary))

# 3rd Method using f i.e Raw string
Name = "Jon Deo"
Age = 26
Salary = 23000
print(f"Name : {Name}  Age: {Age}  Salary {Salary:,d}")