a, b = 10, 20
# 1 - Arithmatic Operators
print(a + b)  # 30
print(a - b)  # -10
print(a * b)  # 200
print(a / b)  # 0.5
print(a % b)  # 10
print(a ** b)  # 100000000000000000000

a, b = 10, 20
# 2 - Logical Operator
print(a < b)  # True
print(a > b)  # False
print(a == b)  # False
print(a <= b)  # True
print(a >= b)  # False
print(a != b)  # True

a, b = 10, 20
# 3 - conditional Operator
print(a and b)  # 20
print(a or b)  # 10
print(not a)  # False
